func TestArea(t *testing.T) {

	areaTests := []struct {
		shape Shape
		want  float64
	}{
		{Rectangle{12, 6}, 72.0},
		{Circle{10}, 314.1592653589793},
		{Triangle{12, 6}, 36.0},
	}

	for _, tt := range areaTests {
		got := tt.shape.Area()
		if got != tt.want {
			//为了使从大量案例中快速的知道是哪个案例出错,使用%#v打印详细字段
			t.Errorf("%#v:got %g want %g", tt.shape, got, tt.want)
		}
	}

}
